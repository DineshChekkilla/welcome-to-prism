package com.prismit.assigments;
//**program for sum of n natural numbers**//
public class SumOfNnumbers {
	public static void main(String[] args) {
		//this is main method//
		int num=58;
		int sum=0;
		for(int i=1;i<=num;i++) {
			sum=sum+i;
		}
		System.out.println("The sum of n value :" +sum);
	}

}
